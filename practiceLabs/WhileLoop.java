package practiceLabs;

public class WhileLoop {

	public static void main(String[] args) 
	{
		
		int integerVariable = 1;
		
		//Check integerVariable value if it is greater than 5
		while(integerVariable < 5)//condition to check
		{
			System.out.println("value of integerVariable:" + integerVariable);//Display
			integerVariable++;//increment integerVariable by 1.
		}
	}
}
