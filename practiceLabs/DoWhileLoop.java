package practiceLabs;

public class DoWhileLoop {

	public static void main(String[] args) 
	{
		int count = 1;
		
		do
		{
			System.out.println("value of integerVariable:" + count);//Display
			count++;//increment integerVariable by 1.
		}
		while(count < 5);//Check integerVariable value if it is greater than 5
	}

}
